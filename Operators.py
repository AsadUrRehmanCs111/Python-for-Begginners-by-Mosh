# addition
print(10 + 2)
# subtraction
print(10 - 2)
# multiplication
print(10 * 2)

# division
print(10 / 2)
# division but it will return integer only
print(10 // 2)
# modulus for remainder
print(10 % 2)
# power operator
print(10 ** 2)

# increment
x = 1
x += 1
print(x)
# decrement
x = 3
x -= 2
print(x)

# operators precedence
# () ** / * + -
x= 20 + 4 - 8 * 6 / 3
print(x)
x = (2 + 3) * 10 - 3
print(x)
